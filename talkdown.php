                            <li>Anstatt „Schlecht“, „nicht so gut“</li>
                            <li>Anstatt „aber“, „nur“</li>
                            <li>Benutze so oft es geht „Vielleicht“, „Wenn du möchtest“, „Ja, nur“ <i>(Auch wenn es sich komisch anhört)</i></li>
